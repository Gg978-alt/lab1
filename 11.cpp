#include <iostream> // підключаємо бібліотеку для введення та виведення
using namespace std;

int main() {
    // Задача: Обчислення радіусу та площі круга
    const double PI = 3.14; // константа числа Пі
    double L, R, S;

    // Введення довжини кола
    cout << "Введіть довжину кола (L): ";
    cin >> L;

    // Обчислення радіусу
    R = L / (2 * PI);

    // Обчислення площі круга
    S = PI * R * R;

    // Виведення результатів
    cout << "Радіус кола: " << R << endl;
    cout << "Площа круга: " << S << endl;

    return 0; // завершення програми
}
